function ABS(x) 
{
    if (x < 0)
        return -(x);
    else
        return x;
}

function VertexInterp(isolevel, p1, p2, valp1, valp2)
{
   var mu;
   var p;

   if (ABS(isolevel-valp1) < 0.00001)
      return(p1);
   if (ABS(isolevel-valp2) < 0.00001)
      return(p2);
   if (ABS(valp1-valp2) < 0.00001)
      return(p1);
   mu = (isolevel - valp1) / (valp2 - valp1);
   p = {x: p1.x + mu * (p2.x - p1.x), y: p1.y + mu * (p2.y - p1.y), z: p1.z + mu * (p2.z - p1.z)};

   return(p);
}

function GradInterp(isolevel, g1, g2, valp1, valp2)
{
    var mu;
    var g;

    if (ABS(isolevel-valp1) < 0.00001)
        return (g1);
    if (ABS(isolevel-valp2) < 0.00001)
        return (g2);
    if (ABS(valp1-valp2) < 0.00001)
        return (g1);

    mu = (isolevel - valp1) / (valp2 - valp1);
    g = {x: g1.x + mu * (g2.x - g1.x), y: g1.y + mu * (g2.y - g1.y), z: g1.z + mu * (g2.z - g1.z)};
    
    return(g);
}

